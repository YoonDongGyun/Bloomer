export const emotionColor = (emotion: string) => {
  switch (emotion) {
    case "기쁨":
      return "#FFE897";
    case "안정":
      return "#C2F9B9";
    case "불안":
      return "#D4AAFA";
    case "당황":
      return "#FFB59E";
    case "슬픔":
      return "#A5DFFF";
    case "상처":
      return "#6972C0";
    case "분노":
      return "#FF8C9B";
  }
};
